package operadores.atribuicao;

public class Introducao {
    public static void main(String[] args) {
        // = , += , -= , *= , /= , %=
        double bonus = 1800; // 1800
        bonus += 1000; // 2800
        bonus -= 1000; // 1800
        bonus *= 2; // 3600
        bonus /= 2; // 1800
        bonus %= 2; // 0

        System.out.println(bonus);
    }
}
